    <script>
        function showInput(type) {
            const trxInput = document.getElementById('trxInput');
            const bscInput = document.getElementById('bscInput');

            if (type === 'trx') {
                trxInput.style.display = 'block';
                bscInput.style.display = 'none';
            } else {
                trxInput.style.display = 'none';
                bscInput.style.display = 'block';
            }
        }

